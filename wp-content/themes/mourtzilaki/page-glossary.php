Α | Αδικοπραξία | Παράνομη και υπαίτια πράξη που προκαλεί ζημία σε άλλον και υποχρεώνει τον δράστη σε αποζημίωση.
Α | Άκυρο | Πράξη που δεν παράγει νομικά αποτελέσματα από την αρχή λόγω παραβίασης ουσιαστικού όρου του νόμου.
