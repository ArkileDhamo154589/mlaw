Δ | Διαθήκη | Η μονομερής δικαιοπραξία με την οποία μια ιδιωτική βούληση ορίζει την τύχη της περιουσίας μετά τον θάνατο.
Δ | Διατροφή | Νομική υποχρέωση παροχής χρηματικού ποσού ή φυσικής φροντίδας σε σύζυγο, παιδιά ή γονείς.
Δ | Διαζύγιο | Η νομική λύση του γάμου. Μπορεί να είναι συναινετικό ή κατ' αντιδικία.
Δ | Διοικητική πράξη | Πράξη Δημόσιας Αρχής που ρυθμίζει συγκεκριμένη περίπτωση. Προσβάλλεται με προσφυγή.
Ε | Έγγραφο τύπου | Ορισμένος έγγραφος τύπος που απαιτεί ο νόμος για την κατάρτιση πράξης (π.χ. συμβολαιογραφικό).
Ε | Έφεση | Ένδικο μέσο με το οποίο ζητείται από ανώτερο δικαστήριο η αναθεώρηση πρωτόδικης απόφασης.
Ε | Επιμέλεια | Το δικαίωμα και η υποχρέωση φροντίδας του προσώπου του τέκνου. Μετά τον ν. 4800/2021 ασκείται κατά κανόνα από κοινού.
Κ | Κατάσχεση | Δέσμευση περιουσιακών στοιχείων του οφειλέτη με σκοπό την αναγκαστική εκτέλεση απαίτησης.
Κ | Κληρονομικό δικαίωμα | Το δικαίωμα ενός προσώπου να αποκτήσει την περιουσία του θανόντος, εκ διαθήκης ή εξ αδιαθέτου.
