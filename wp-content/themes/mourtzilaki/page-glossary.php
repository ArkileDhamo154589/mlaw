Ν | Νόμιμη μοίρα | Το ελάχιστο ποσοστό της κληρονομιάς που εξασφαλίζεται για συζύγους, γονείς και τέκνα ανεξαρτήτως διαθήκης.
Ν | Νομολογία | Το σύνολο των δικαστικών αποφάσεων που αποτελούν οδηγό για την ερμηνεία του δικαίου.
