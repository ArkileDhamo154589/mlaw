$rows = mourtzilaki_parse_lines( $g( 'glossary_terms', $default_terms_text ) );

// Group rows by letter (preserves first-seen letter order).
$terms = array();
foreach ( $rows as $row ) {
    $letter = $row[0] ?? '';
    $term   = $row[1] ?? '';
    $def    = $row[2] ?? '';
    if ( '' === $letter || '' === $term ) { continue; }
    $terms[ $letter ][] = array( $term, $def );
}
?>

<section class="page-header">
    <div class="container container-narrow">
        <span class="eyebrow"><?php echo esc_html( ! empty( $h['eyebrow'] ) ? $h['eyebrow'] : 'Νομικό λεξικό' ); ?></span>
        <h1 class="h-1 mt-2"><?php echo esc_html( ! empty( $h['title'] ) ? $h['title'] : 'Βασικοί νομικοί όροι, σε απλή γλώσσα.' ); ?></h1>
        <p class="lead"><?php echo ! empty( $h['lead'] ) ? mourtzilaki_field_inline( $h['lead'] ) : esc_html( 'Ένα γρήγορο γλωσσάρι με τους όρους που συναντάει συχνότερα ένας ιδιώτης ή επιχειρηματίας στις υποθέσεις του.' ); ?></p>
    </div>
</section>

<?php if ( ! empty( $terms ) ) : ?>
<section class="section section-tight">
    <div class="container container-narrow">
        <nav class="glossary-nav reveal reveal-up" aria-label="Αλφαβητική πλοήγηση">
            <?php foreach ( array_keys( $terms ) as $letter ) : ?>
                <a href="#l-<?php echo esc_attr( $letter ); ?>"><?php echo esc_html( $letter ); ?></a>
            <?php endforeach; ?>
        </nav>

        <?php foreach ( $terms as $letter => $entries ) : ?>
            <section id="l-<?php echo esc_attr( $letter ); ?>" class="glossary-block reveal reveal-up">
                <h2 class="glossary-letter"><?php echo esc_html( $letter ); ?></h2>
                <dl class="glossary-list">
                    <?php foreach ( $entries as $row ) : ?>
                        <div class="glossary-item">
                            <dt><?php echo esc_html( $row[0] ); ?></dt>
                            <dd><?php echo esc_html( $row[1] ); ?></dd>
                        </div>
                    <?php endforeach; ?>
                </dl>
            </section>
        <?php endforeach; ?>
    </div>
</section>
<?php endif; ?>

